import {trigger, state, animate, style, transition} from '@angular/animations';
//routerTransition
export function routerTransition() {
  return slideToTop();
}

export function routerTransitionBottom() {
  return slideToBottom();
}

export function routerTransitionLeft() {
  return slideToLeft();
}

export function routerTransitionRight() {
  return slideToRight();
}

function slideToRight() {
  return trigger('routerTransition', [
    state('void', style({position:'fixed', width:'100%'}) ),
    state('*', style({position:'fixed', width:'100%'}) ),
    transition(':enter', [
      style({transform: 'translateX(-100%)'}),
      animate('2s ease-in-out', style({transform: 'translateX(0%)'}))
    ]),
    transition(':leave', [
      style({transform: 'translateX(0%)'}),
      animate('2s ease-in-out', style({transform: 'translateX(100%)'}))
    ])
  ]);
}

